    private void btnStatisticByYearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStatisticByYearActionPerformed
        if (txtStartYear.getText().isEmpty() || txtEndYear.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Do not leave the field blank !",
                "Error message", JOptionPane.ERROR_MESSAGE);
        }

        int startYear = Integer.parseInt(txtStartYear.getText());
        int endYear = Integer.parseInt(txtEndYear.getText());

        RevenueStatisticsBUS revenueStatisticsBUS = new RevenueStatisticsBUS();
        LocalDate startDate = LocalDate.of(startYear, 1, 1);
        LocalDate endDate = LocalDate.of(endYear, 12, 31);
        List<RevenueDataPoint> dataPoints = revenueStatisticsBUS.getYearlyRevenue(startDate, endDate);

        double totalRevenue = 0;
        for (RevenueDataPoint dataPoint : dataPoints) {
            totalRevenue += dataPoint.getRevenue();
        }

        txtTotalRevenue.setText("%.2f".formatted(totalRevenue));
        txtTotalOrders.setText(Integer.toString(dataPoints.size()));

        drawChart(dataPoints, startDate, endDate, "Year");
    }//GEN-LAST:event_btnStatisticByYearActionPerformed

