    private void drawChart(List<RevenueDataPoint> dataPoints, LocalDate startDate, LocalDate endDate, String timeUnit) {

        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        for (RevenueDataPoint dataPoint : dataPoints) {
            dataset.addValue(dataPoint.getRevenue(), "Revenue", dataPoint.getLabel());
        }

        JFreeChart chart = ChartFactory.createBarChart(
                "Revenue " + timeUnit + " (" + startDate + " - " + endDate + ")",
                timeUnit,
                "Revenue (VND)",
                dataset, // data
                PlotOrientation.VERTICAL,
                false,
                true,
                false
        );

        CategoryPlot plot = chart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.BLACK);

        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setSize(jPanel5.getWidth(), jPanel5.getHeight());
        chartPanel.setPreferredSize(new Dimension(jPanel5.getWidth(), jPanel5.getHeight()));
        chartPanel.setVisible(true);
        jPanel5.removeAll();
        jPanel5.add(chartPanel);
        jPanel5.revalidate();
        jPanel5.repaint();
    }

